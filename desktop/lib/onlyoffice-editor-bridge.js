const PRESENTATION_BRIDGE_GUID = "asc.{7F90C4D8-9B66-4C44-9015-22C8C2A2D84F}";
const PRESENTATION_BRIDGE_PLUGIN_CONFIG_PATH = "/sdkjs-plugins/presentation-ai-bridge/config.json";
const PRESENTATION_BRIDGE_PLUGIN_PATH_FRAGMENT = "/sdkjs-plugins/presentation-ai-bridge/";
const CONNECT_EDITOR_MARKER = "        var connectEditor = function () {";
const DEFAULT_ONLYOFFICE_ORIGINS = ["http://localhost:8080", "http://127.0.0.1:8080"];

function buildOnlyOfficeEditorUrl(onlyofficeOrigin, searchParams = {}) {
  const url = new URL("/example/editor", String(onlyofficeOrigin || "http://localhost:8080"));
  Object.entries(searchParams).forEach(([key, value]) => {
    if (value === undefined || value === null || value === "") return;
    url.searchParams.set(key, String(value));
  });
  return url.toString();
}

function buildPresentationBridgeBootstrap(options = {}) {
  const bridgeWaitTimeoutMs = Number.isFinite(options.bridgeWaitTimeoutMs)
    ? Math.max(0, Math.floor(options.bridgeWaitTimeoutMs))
    : 2200;
  const bridgePollIntervalMs = Number.isFinite(options.bridgePollIntervalMs)
    ? Math.max(10, Math.floor(options.bridgePollIntervalMs))
    : 120;
  const bridgeRequestWatchdogMs = Number.isFinite(options.bridgeRequestWatchdogMs)
    ? Math.max(500, Math.floor(options.bridgeRequestWatchdogMs))
    : 5200;
  const bridgeTransientRetryLimit = Number.isFinite(options.bridgeTransientRetryLimit)
    ? Math.max(0, Math.floor(options.bridgeTransientRetryLimit))
    : 2;
  const bridgeTransientRetryDelayMs = Number.isFinite(options.bridgeTransientRetryDelayMs)
    ? Math.max(10, Math.floor(options.bridgeTransientRetryDelayMs))
    : 180;
  const bridgeTotalBudgetMs = Number.isFinite(options.bridgeTotalBudgetMs)
    ? Math.max(1000, Math.floor(options.bridgeTotalBudgetMs))
    : 7600;
  const payload = {
    bridgeGuid: options.bridgeGuid || PRESENTATION_BRIDGE_GUID,
    bridgePluginConfigPath: options.bridgePluginConfigPath || PRESENTATION_BRIDGE_PLUGIN_CONFIG_PATH,
    bridgePluginPathFragment: options.bridgePluginPathFragment || PRESENTATION_BRIDGE_PLUGIN_PATH_FRAGMENT,
  };

  return [
    "        (function () {",
    `          var PRESENTATION_BRIDGE = ${JSON.stringify(payload)};`,
    `          var BRIDGE_WAIT_TIMEOUT_MS = ${bridgeWaitTimeoutMs};`,
    `          var BRIDGE_POLL_INTERVAL_MS = ${bridgePollIntervalMs};`,
    `          var BRIDGE_REQUEST_WATCHDOG_MS = ${bridgeRequestWatchdogMs};`,
    `          var BRIDGE_TRANSIENT_RETRY_LIMIT = ${bridgeTransientRetryLimit};`,
    `          var BRIDGE_TRANSIENT_RETRY_DELAY_MS = ${bridgeTransientRetryDelayMs};`,
    `          var BRIDGE_TOTAL_BUDGET_MS = ${bridgeTotalBudgetMs};`,
    "          var editorReadySent = false;",
    "          var editorErrorSent = false;",
    "          var presentationBridgeWindow = null;",
    "          var pendingBridgeRequests = Object.create(null);",
    "",
    "          function getDocumentType() {",
    "            return window.config && window.config.documentType ? String(window.config.documentType) : '';",
    "          }",
    "",
    "          function isSlideEditor() {",
    "            return getDocumentType() === 'slide';",
    "          }",
    "",
    "          function postToParent(payload) {",
    "            try {",
    "              window.parent.postMessage(payload, '*');",
    "            } catch (_error) {}",
    "          }",
    "",
    "          function notifyEditorReady() {",
    "            if (editorReadySent) return;",
    "            editorReadySent = true;",
    "            postToParent({ type: 'onlyoffice-editor-ready', documentType: getDocumentType() });",
    "          }",
    "",
    "          function notifyEditorError(message) {",
    "            if (editorReadySent || editorErrorSent) return;",
    "            editorErrorSent = true;",
    "            postToParent({",
    "              type: 'onlyoffice-editor-error',",
    "              documentType: getDocumentType(),",
    "              message: message || 'OnlyOffice editor failed to initialize.',",
    "            });",
    "          }",
    "",
    "          function isBridgeGuidMatch(candidateGuid) {",
    "            return String(candidateGuid || '') === String(PRESENTATION_BRIDGE.bridgeGuid || '');",
    "          }",
    "",
    "          function setPresentationBridgeWindow(candidate) {",
    "            if (!candidate) return;",
    "            presentationBridgeWindow = candidate;",
    "            window.__excelorPresentationBridgeWindow = candidate;",
    "          }",
    "",
    "          function ensurePluginsConfig() {",
    "            if (!isSlideEditor()) return;",
    "            window.config.editorConfig = window.config.editorConfig || {};",
    "            window.config.editorConfig.plugins = window.config.editorConfig.plugins || {};",
    "            var plugins = window.config.editorConfig.plugins;",
    "            plugins.pluginsData = Array.isArray(plugins.pluginsData) ? plugins.pluginsData.slice() : [];",
    "            var pluginConfigUrl = new URL(PRESENTATION_BRIDGE.bridgePluginConfigPath, window.location.origin).toString();",
    "            if (plugins.pluginsData.indexOf(pluginConfigUrl) === -1) {",
    "              plugins.pluginsData.push(pluginConfigUrl);",
    "            }",
    "            var autostart = Array.isArray(plugins.autostart) ? plugins.autostart.slice() : [];",
    "            if (autostart.indexOf(PRESENTATION_BRIDGE.bridgeGuid) === -1) {",
    "              autostart.push(PRESENTATION_BRIDGE.bridgeGuid);",
    "            }",
    "            plugins.autostart = autostart;",
    "          }",
    "",
    "          function pushUniqueWindow(list, candidate) {",
    "            if (!candidate) return;",
    "            if (list.indexOf(candidate) >= 0) return;",
    "            list.push(candidate);",
    "          }",
    "",
    "          function collectAccessibleFrames(rootWindow, frames) {",
    "            var list = frames || [];",
    "            var frameElements;",
    "            try {",
    "              frameElements = rootWindow.document.querySelectorAll('iframe');",
    "            } catch (_error) {",
    "              return list;",
    "            }",
    "            for (var i = 0; i < frameElements.length; i++) {",
    "              var frameWindow = frameElements[i].contentWindow;",
    "              if (!frameWindow || list.indexOf(frameWindow) >= 0) continue;",
    "              list.push(frameWindow);",
    "              try {",
    "                collectAccessibleFrames(frameWindow, list);",
    "              } catch (_error) {}",
    "            }",
    "            return list;",
    "          }",
    "",
    "          function getBridgeCandidates() {",
    "            var candidates = [];",
    "            pushUniqueWindow(candidates, presentationBridgeWindow);",
    "            pushUniqueWindow(candidates, window.__excelorPresentationBridgeWindow);",
    "            pushUniqueWindow(candidates, window);",
    "            var frames = collectAccessibleFrames(window, []);",
    "            for (var i = 0; i < frames.length; i++) {",
    "              pushUniqueWindow(candidates, frames[i]);",
    "            }",
    "            return candidates;",
    "          }",
    "",
    "          function hasDirectBridgeApi(candidate) {",
    "            try {",
    "              return !!(",
    "                candidate &&",
    "                candidate.PresentationBridgeCore &&",
    "                typeof candidate.PresentationBridgeCore.executePresentationRequest === 'function'",
    "              );",
    "            } catch (_error) {",
    "              return false;",
    "            }",
    "          }",
    "",
    "          function findPresentationBridgeCandidate() {",
    "            var candidates = getBridgeCandidates();",
    "            var directCandidate = null;",
    "            var urlCandidate = null;",
    "",
    "            for (var i = 0; i < candidates.length; i++) {",
    "              var candidate = candidates[i];",
    "              if (!directCandidate && hasDirectBridgeApi(candidate)) {",
    "                directCandidate = candidate;",
    "                break;",
    "              }",
    "",
    "              if (urlCandidate) continue;",
    "              try {",
    "                var href = candidate.location && candidate.location.href ? String(candidate.location.href) : '';",
    "                if (href.indexOf(PRESENTATION_BRIDGE.bridgePluginPathFragment) >= 0) {",
    "                  urlCandidate = candidate;",
    "                }",
    "              } catch (_error) {}",
    "            }",
    "",
    "            var bridgeWindow = directCandidate || urlCandidate || null;",
    "            if (bridgeWindow) {",
    "              setPresentationBridgeWindow(bridgeWindow);",
    "            }",
    "",
    "            return {",
    "              bridgeWindow: bridgeWindow,",
    "              candidateCount: candidates.length,",
    "              mode: directCandidate ? 'direct' : (urlCandidate ? 'postMessage' : 'none'),",
    "            };",
    "          }",
    "",
    "          function normalizeBridgeResult(rawResult) {",
    "            if (!rawResult) {",
    "              return { success: false, message: 'The presentation bridge returned no result.' };",
    "            }",
    "",
    "            if (typeof rawResult === 'string') {",
    "              try {",
    "                rawResult = JSON.parse(rawResult);",
    "              } catch (_error) {",
    "                return { success: false, message: rawResult };",
    "              }",
    "            }",
    "",
    "            if (typeof rawResult === 'object') {",
    "              return {",
    "                success: rawResult.success === true,",
    "                message: typeof rawResult.message === 'string'",
    "                  ? rawResult.message",
    "                  : (rawResult.success === true ? 'Presentation tool completed.' : 'Presentation tool failed.'),",
    "                data: rawResult.data,",
    "              };",
    "            }",
    "",
    "            return { success: false, message: String(rawResult) };",
    "          }",
    "",
    "          function mergeDiagnosticsData(source, diagnostics) {",
    "            var base = source && typeof source === 'object' ? source : {};",
    "            var merged = {};",
    "            var key;",
    "            for (key in base) {",
    "              if (Object.prototype.hasOwnProperty.call(base, key)) {",
    "                merged[key] = base[key];",
    "              }",
    "            }",
    "            for (key in diagnostics) {",
    "              if (Object.prototype.hasOwnProperty.call(diagnostics, key) && diagnostics[key] !== undefined) {",
    "                merged[key] = diagnostics[key];",
    "              }",
    "            }",
    "            return merged;",
    "          }",
    "",
    "          function clearPendingBridgeRequest(requestId) {",
    "            var pending = pendingBridgeRequests[requestId];",
    "            if (!pending) return null;",
    "            if (pending.timeoutId) {",
    "              window.clearTimeout(pending.timeoutId);",
    "            }",
    "            delete pendingBridgeRequests[requestId];",
    "            return pending;",
    "          }",
    "",
    "          function registerPendingBridgeRequest(requestId, onResult) {",
    "            if (!requestId) return false;",
    "            clearPendingBridgeRequest(requestId);",
    "            var timeoutId = window.setTimeout(function () {",
    "              clearPendingBridgeRequest(requestId);",
    "              onResult(",
    "                { success: false, message: 'Timed out waiting for the OnlyOffice presentation bridge.' },",
    "                { bridgeTimeoutMs: BRIDGE_REQUEST_WATCHDOG_MS, reason: 'bridge_watchdog_timeout' }",
    "              );",
    "            }, BRIDGE_REQUEST_WATCHDOG_MS);",
    "            pendingBridgeRequests[requestId] = { timeoutId: timeoutId, onResult: onResult };",
    "            return true;",
    "          }",
    "",
    "          function isTransientBridgeFailure(message) {",
    "            var value = String(message || '').toLowerCase();",
    "            if (!value) return false;",
    "            return (",
    "              value.indexOf('plugin runtime is unavailable') >= 0 ||",
    "              value.indexOf('presentation api is unavailable') >= 0 ||",
    "              value.indexOf('bridge plugin is not available') >= 0 ||",
    "              value.indexOf('still loading') >= 0",
    "            );",
    "          }",
    "",
    "          function executeBridgeDirect(bridgeWindow, message, onResult) {",
    "            if (!hasDirectBridgeApi(bridgeWindow)) return false;",
    "",
    "            try {",
    "              bridgeWindow.PresentationBridgeCore.executePresentationRequest({",
    "                type: message.type,",
    "                requestId: message.requestId,",
    "                action: message.action,",
    "                params: message.params,",
    "                actions: message.actions,",
    "              }, function (rawResult) {",
    "                onResult(normalizeBridgeResult(rawResult), {});",
    "              });",
    "              return true;",
    "            } catch (error) {",
    "              onResult(",
    "                { success: false, message: error && error.message ? error.message : String(error) },",
    "                { reason: 'bridge_direct_exception' }",
    "              );",
    "              return true;",
    "            }",
    "          }",
    "",
    "          function executeBridgePostMessage(bridgeWindow, message, onResult) {",
    "            if (!bridgeWindow || bridgeWindow === window) return false;",
    "            if (typeof bridgeWindow.postMessage !== 'function') return false;",
    "            if (!message || !message.requestId) return false;",
    "            var requestId = String(message.requestId);",
    "            if (!registerPendingBridgeRequest(requestId, onResult)) return false;",
    "",
    "            try {",
    "              bridgeWindow.postMessage(message, '*');",
    "              return true;",
    "            } catch (error) {",
    "              clearPendingBridgeRequest(requestId);",
    "              onResult(",
    "                { success: false, message: error && error.message ? error.message : String(error) },",
    "                { reason: 'bridge_postmessage_exception' }",
    "              );",
    "              return true;",
    "            }",
    "          }",
    "",
    "          function relayParentMessage(message) {",
    "            var startedAt = Date.now();",
    "            var retryAttempts = 0;",
    "            var completed = false;",
    "",
    "            function elapsedMs() {",
    "              return Date.now() - startedAt;",
    "            }",
    "",
    "            function finalizeResult(result, diagnostics) {",
    "              if (completed) return;",
    "              completed = true;",
    "              var normalized = normalizeBridgeResult(result);",
    "              postToParent({",
    "                type: 'tool-result',",
    "                requestId: message.requestId,",
    "                success: normalized.success === true,",
    "                message: normalized.message,",
    "                data: mergeDiagnosticsData(normalized.data, diagnostics),",
    "              });",
    "            }",
    "",
    "            function shouldRetryTransient(result) {",
    "              if (!result || result.success === true) return false;",
    "              if (!isTransientBridgeFailure(result.message)) return false;",
    "              if (retryAttempts >= BRIDGE_TRANSIENT_RETRY_LIMIT) return false;",
    "              if (elapsedMs() >= BRIDGE_TOTAL_BUDGET_MS) return false;",
    "              return true;",
    "            }",
    "",
    "            function handleBridgeResult(result, diagnostics) {",
    "              if (completed) return;",
    "              var normalized = normalizeBridgeResult(result);",
    "              if (shouldRetryTransient(normalized)) {",
    "                retryAttempts += 1;",
    "                window.setTimeout(attemptBridgeRelay, BRIDGE_TRANSIENT_RETRY_DELAY_MS);",
    "                return;",
    "              }",
    "              finalizeResult(normalized, mergeDiagnosticsData(diagnostics, {",
    "                retryAttempts: retryAttempts,",
    "                waitMs: elapsedMs(),",
    "              }));",
    "            }",
    "",
    "            function attemptBridgeRelay() {",
    "              if (completed) return;",
    "              if (elapsedMs() >= BRIDGE_TOTAL_BUDGET_MS) {",
    "                finalizeResult(",
    "                  { success: false, message: 'Timed out waiting for the OnlyOffice presentation bridge.' },",
    "                  {",
    "                    relayMode: 'none',",
    "                    retryAttempts: retryAttempts,",
    "                    waitMs: elapsedMs(),",
    "                    bridgeTimeoutMs: BRIDGE_TOTAL_BUDGET_MS,",
    "                    reason: 'bridge_total_budget_timeout',",
    "                  }",
    "                );",
    "                return;",
    "              }",
    "",
    "              var resolved = findPresentationBridgeCandidate();",
    "              var bridgeWindow = resolved.bridgeWindow;",
    "",
    "              if (bridgeWindow && executeBridgeDirect(bridgeWindow, message, function (result, extra) {",
    "                handleBridgeResult(result, mergeDiagnosticsData(extra, {",
    "                  relayMode: 'direct',",
    "                  candidateCount: resolved.candidateCount,",
    "                }));",
    "              })) {",
    "                return;",
    "              }",
    "",
    "              if (bridgeWindow && executeBridgePostMessage(bridgeWindow, message, function (result, extra) {",
    "                handleBridgeResult(result, mergeDiagnosticsData(extra, {",
    "                  relayMode: 'postMessage',",
    "                  candidateCount: resolved.candidateCount,",
    "                  bridgeTimeoutMs: BRIDGE_REQUEST_WATCHDOG_MS,",
    "                }));",
    "              })) {",
    "                return;",
    "              }",
    "",
    "              var waitedMs = elapsedMs();",
    "              if (waitedMs < BRIDGE_WAIT_TIMEOUT_MS) {",
    "                window.setTimeout(attemptBridgeRelay, BRIDGE_POLL_INTERVAL_MS);",
    "                return;",
    "              }",
    "",
    "              finalizeResult(",
    "                {",
    "                  success: false,",
    "                  message:",
    "                    'Presentation bridge plugin is not available in the editor. ' +",
    "                    '(waited ' + waitedMs + 'ms; checked ' + resolved.candidateCount + ' candidate windows.)',",
    "                },",
    "                {",
    "                  relayMode: resolved.mode,",
    "                  retryAttempts: retryAttempts,",
    "                  waitMs: waitedMs,",
    "                  candidateCount: resolved.candidateCount,",
    "                  reason: 'bridge_unavailable_after_wait',",
    "                }",
    "              );",
    "            }",
    "",
    "            attemptBridgeRelay();",
    "          }",
    "",
    "          function monitorEditorStartup() {",
    "            var startedAt = Date.now();",
    "            function check() {",
    "              try {",
    "                var editorRoot = window.document.getElementById('iframeEditor');",
    "                var nestedFrame = editorRoot && editorRoot.querySelector ? editorRoot.querySelector('iframe') : null;",
    "                if (nestedFrame || window.docEditor) {",
    "                  notifyEditorReady();",
    "                  return;",
    "                }",
    "                if (Date.now() - startedAt >= 10000) {",
    "                  notifyEditorError('OnlyOffice editor runtime did not initialize.');",
    "                  return;",
    "                }",
    "                window.setTimeout(check, 250);",
    "              } catch (error) {",
    "                notifyEditorError(error && error.message ? error.message : 'OnlyOffice editor bootstrap failed.');",
    "              }",
    "            }",
    "            window.setTimeout(check, 0);",
    "          }",
    "",
    "          window.addEventListener('message', function (event) {",
    "            var message = event && event.data;",
    "            if (!message || !message.type) return;",
    "",
    "            if (message.type === 'presentation-bridge-ready' && event.source !== window.parent) {",
    "              if (!isSlideEditor()) return;",
    "              if (!isBridgeGuidMatch(message.bridgeGuid)) return;",
    "              setPresentationBridgeWindow(event.source || null);",
    "              postToParent({",
    "                type: 'presentation-bridge-ready',",
    "                bridgeGuid: PRESENTATION_BRIDGE.bridgeGuid,",
    "                documentType: getDocumentType(),",
    "              });",
    "              return;",
    "            }",
    "",
    "            if (message.type === 'apply-actions' || message.type === 'request-read') {",
    "              if (event.source !== window.parent) return;",
    "              if (!isSlideEditor()) {",
    "                postToParent({",
    "                  type: 'tool-result',",
    "                  requestId: message.requestId,",
    "                  success: false,",
    "                  message: 'Automation bridge unavailable for this editor context.',",
    "                });",
    "                return;",
    "              }",
    "              relayParentMessage(message);",
    "              return;",
    "            }",
    "",
    "            if (message.type === 'tool-result' && event.source !== window.parent) {",
    "              var requestId = typeof message.requestId === 'string' ? message.requestId : '';",
    "              var pending = requestId ? clearPendingBridgeRequest(requestId) : null;",
    "              if (pending && typeof pending.onResult === 'function') {",
    "                pending.onResult(normalizeBridgeResult(message), { reason: 'bridge_tool_result' });",
    "                return;",
    "              }",
    "              postToParent(message);",
    "            }",
    "          });",
    "",
    "          window.addEventListener('error', function (event) {",
    "            notifyEditorError(event && event.message ? event.message : 'OnlyOffice editor reported a script error.');",
    "          });",
    "",
    "          window.addEventListener('unhandledrejection', function (event) {",
    "            var reason = event ? event.reason : null;",
    "            var message = reason && reason.message ? reason.message : String(reason || 'OnlyOffice editor reported an unhandled rejection.');",
    "            notifyEditorError(message);",
    "          });",
    "",
    "          window.__excelorOnlyOfficeConnectEditorHook = function () {",
    "            try {",
    "              ensurePluginsConfig();",
    "              monitorEditorStartup();",
    "            } catch (error) {",
    "              notifyEditorError(error && error.message ? error.message : 'OnlyOffice editor bootstrap failed.');",
    "            }",
    "          };",
    "        })();",
  ].join("\n");
}

function normalizeOrigin(origin) {
  const value = String(origin || "").trim();
  if (!value) return "";
  return value.endsWith("/") ? value.slice(0, -1) : value;
}

function rewriteOnlyOfficeServiceUrls(html, options = {}) {
  if (typeof html !== "string" || !html) {
    return html;
  }

  const containerOrigin = normalizeOrigin(options.containerOrigin);
  if (!containerOrigin) {
    return html;
  }

  const browserOrigins = Array.from(
    new Set(
      (Array.isArray(options.browserOrigins) ? options.browserOrigins : DEFAULT_ONLYOFFICE_ORIGINS)
        .map(normalizeOrigin)
        .filter(Boolean),
    ),
  );

  return browserOrigins.reduce((result, browserOrigin) => {
    let next = result;
    next = next.replace(
      new RegExp(`(\"url\"\\s*:\\s*\")${browserOrigin.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}(\\/example\\/download[^"]*\")`, "g"),
      `$1${containerOrigin}$2`,
    );
    next = next.replace(
      new RegExp(`(\"callbackUrl\"\\s*:\\s*\")${browserOrigin.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}(\\/example\\/track[^"]*\")`, "g"),
      `$1${containerOrigin}$2`,
    );
    return next;
  }, html);
}

function injectOnlyOfficeEditorBootstrap(html, options = {}) {
  if (typeof html !== "string" || !html.includes(CONNECT_EDITOR_MARKER)) {
    return html;
  }

  const bootstrap = buildPresentationBridgeBootstrap(options);
  return html.replace(
    CONNECT_EDITOR_MARKER,
    `${bootstrap}\n\n${CONNECT_EDITOR_MARKER}\n          if (typeof window.__excelorOnlyOfficeConnectEditorHook === 'function') window.__excelorOnlyOfficeConnectEditorHook();`,
  );
}

function isLocalOnlyOfficeBridgePath(pathname = "") {
  return pathname === "/browser/action" || pathname === "/editor/tool";
}

function isOnlyOfficeEditorRequest(rawUrl, options = {}) {
  try {
    const parsed = rawUrl instanceof URL ? rawUrl : new URL(String(rawUrl || ""));
    const onlyofficePort = String(options.onlyofficePort || "8080");
    const hostname = String(parsed.hostname || "").toLowerCase();
    return (
      (hostname === "127.0.0.1" || hostname === "localhost") &&
      String(parsed.port || "80") === onlyofficePort &&
      parsed.pathname === "/example/editor"
    );
  } catch (_error) {
    return false;
  }
}

